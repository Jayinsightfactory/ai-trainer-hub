export async function runContextWeaver(
  input: ContextWeaverInput
): Promise<ContextWeaverOutput> {
  // Simulate processing delay for realistic UX
  await new Promise((resolve) => setTimeout(resolve, 1500));

  const knowledge = INDUSTRY_KNOWLEDGE[input.industry] ?? INDUSTRY_KNOWLEDGE.cafe;

  const systemPrompt = generateSystemPrompt(
    input.industry,
    input.businessName,
    input.purpose
  );

  // Calculate quality score based on available data
  let qualityScore = 72; // base score from mock research
  if (input.existingData && input.existingData.length > 0) {
    qualityScore += Math.min(input.existingData.length * 5, 20);
  }
  // Cap at 95 for MVP (real data would push higher)
  qualityScore = Math.min(qualityScore, 95);

  return {
    systemPrompt,
    knowledge,
    qualityScore,
  };
}
