export interface TrainingConversation {
  id: string;
  difficulty: "easy" | "medium" | "hard";
  category: string;
  userMessage: string;
  expectedResponse: string;
  tags: string[];
}

export interface EdgeCase {
  id: string;
  type: string;
  scenario: string;
  userMessage: string;
  correctResponse: string;
  incorrectResponse: string;
  reason: string;
}

export interface ErrorScenario {
  id: string;
  type: string;
  description: string;
  wrongBehavior: string;
  correctBehavior: string;
  severity: "low" | "medium" | "high" | "critical";
}

export interface Department {
  name: string;
  specialty: string[];
  commonSymptoms: string[];
  location: string;
}

export interface DoctorProfile {
  name: string;
  department: string;
  specialty: string;
  education: string;
  schedule: Record<string, string>;
  languages: string[];
}

export interface PreVisitGuide {
  department: string;
  preparations: string[];
  documents: string[];
  fasting: string | null;
  notes: string;
}

